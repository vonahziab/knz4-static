const emoji: Record<string, string> = {
	'1': '&#49;&#65039;&#8419;',
	'2': '&#50;&#65039;&#8419;',
	'3': '&#51;&#65039;&#8419;',
	'4': '&#52;&#65039;&#8419;',
	'5': '&#53;&#65039;&#8419;',
	'6': '&#54;&#65039;&#8419;',
	'7': '&#55;&#65039;&#8419;',
	'8': '&#56;&#65039;&#8419;',
	'9': '&#57;&#65039;&#8419;',
	'0': '&#48;&#65039;&#8419;',
};

const numbersToEmoji = (number: number) => {
	return `${number}`
		.split('')
		.map(char => emoji[char])
		.join('');
};

export default numbersToEmoji;
